    private void configurarTabla() {
        modeloTabla = new DefaultTableModel(
            new String[]{"ID", "Nombre", "Descripción", "Cantidad", "Precio"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int col) { return false; }
        };
        tbl_productos.setModel(modeloTabla);
        tbl_productos.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
    }
 
    private void configurarSeleccionTabla() {
        tbl_productos.getSelectionModel().addListSelectionListener(e -> {
            int fila = tbl_productos.getSelectedRow();
            if (fila >= 0) {
                txt_id.setText(String.valueOf(modeloTabla.getValueAt(fila, 0)));
                txt_nombre.setText((String) modeloTabla.getValueAt(fila, 1));
                txt_descripcion.setText((String) modeloTabla.getValueAt(fila, 2));
                txt_cantidad.setText(String.valueOf(modeloTabla.getValueAt(fila, 3)));
                txt_precio.setText(String.valueOf(modeloTabla.getValueAt(fila, 4)));
            }
        });
    }
 
    private void cargarTabla() {
        modeloTabla.setRowCount(0);
        List<Producto> lista = productoDAO.listarTodos();
        for (Producto p : lista) {
            modeloTabla.addRow(new Object[]{
                p.getId(), p.getNombre(), p.getDescripcion(),
                p.getCantidad(), p.getPrecio()
            });
        }
    }
 
    private boolean validar() {
        if (txt_nombre.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "El nombre es obligatorio.", "Error",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return false;
        }
        try {
            Integer.parseInt(txt_cantidad.getText().trim());
            Double.parseDouble(txt_precio.getText().trim());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Cantidad y precio deben ser números.", "Error",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }
 
    private void limpiar() {
        txt_id.setText("");
        txt_nombre.setText("");
        txt_descripcion.setText("");
        txt_cantidad.setText("");
        txt_precio.setText("");
        tbl_productos.clearSelection();
    }

