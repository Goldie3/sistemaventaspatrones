    private void bt_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_actualizarActionPerformed
        if (tbl_productos.getSelectedRow() < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un producto de la tabla.");
            return;
        }
        if (!validar()) return;
        Producto p = new Producto(
            txt_nombre.getText().trim(),
            txt_descripcion.getText().trim(),
            Integer.parseInt(txt_cantidad.getText().trim()),
            Double.parseDouble(txt_precio.getText().trim())
        );
        productoDAO.actualizar(p);
        javax.swing.JOptionPane.showMessageDialog(this, "Producto actualizado correctamente.");
        limpiar();
        cargarTabla();
    }//GEN-LAST:event_bt_actualizarActionPerformed

    private void bt_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_eliminarActionPerformed
        int fila = tbl_productos.getSelectedRow();
        if (fila < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un producto de la tabla.");
            return;
        }
        int confirmar = javax.swing.JOptionPane.showConfirmDialog(this,
            "¿Eliminar este producto?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirmar == javax.swing.JOptionPane.YES_OPTION) {
            String nombre = (String) modeloTabla.getValueAt(fila, 1);
            productoDAO.eliminar(nombre);
            javax.swing.JOptionPane.showMessageDialog(this, "Producto eliminado.");
            limpiar();
            cargarTabla();
        }
    }//GEN-LAST:event_bt_eliminarActionPerformed

    private void bt_cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cancelarActionPerformed
        limpiar();
    }//GEN-LAST:event_bt_cancelarActionPerformed

    private void bt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardarActionPerformed
        if (!validar()) return;
        Producto p = EntidadFactory.crearProducto(
            txt_nombre.getText().trim(),
            txt_descripcion.getText().trim(),
            Integer.parseInt(txt_cantidad.getText().trim()),
            Double.parseDouble(txt_precio.getText().trim())
        );
        productoDAO.guardar(p);
        javax.swing.JOptionPane.showMessageDialog(this, "Producto guardado correctamente.");
        limpiar();
        cargarTabla();
    }//GEN-LAST:event_bt_guardarActionPerformed


