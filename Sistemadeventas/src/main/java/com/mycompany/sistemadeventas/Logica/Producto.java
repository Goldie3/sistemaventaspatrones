package com.mycompany.sistemadeventas.Logica;

/**
 *
 * @author bruno
 */
public class Producto {
    public int id;
    private String nombre;
    private String descripcion;
    private int cantidad;
    private double precio;

    public Producto(String nombre, String descripcion, int cantidad, double precio) {
        this.nombre = nombre;
        this.descripcion = descripcion;
        this.cantidad = cantidad;
        this.precio = precio;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    /**
     * Usado por JComboBox (y cualquier otro lugar) para mostrar
     * una representación legible del producto.
     */
    @Override
    public String toString() {
        return nombre + " - $" + precio + " (stock: " + cantidad + ")";
    }
}